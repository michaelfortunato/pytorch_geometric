def test_edge_adjaency_matrix_for_boundary_edge(simplest_non_watertight_mesh):
    pos, face = simplest_non_watertight_mesh
    assert isinstance(pos, torch.Tensor) and isinstance(face, torch.Tensor)
    ff = MeshCNN.FeatureExtractionLayer()
    data = ff.forward(Data(pos=pos, face=face))  # noqa: reportArgumentType
    assert data.x is not None and data.edge_index is not None
    assert data.x.size(0) == data.edge_index.size(1)//4, \
        "X^(1) should be shape(|E|, 4) and A should be shape (2, 4*|E|)"
    print(data.edge_index.shape)


