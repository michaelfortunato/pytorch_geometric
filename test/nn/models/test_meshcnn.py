    edges, edge_adjacency, _ = MeshCNN.FeatureExtractionLayer.edge_adjacency(
        face)
    # we know the first edge is a bondary edge, so just assert the
    # property that we enforce in line with the orignal paper
    # ref: https://github.com/ranahanocka/MeshCNN/blob/5bf0b899d48eb204b9b73bc1af381be20f4d7df1/models/layers/mesh_prepare.py#L384 # noqa: E501
    assert edge_adjacency[0, 0] == edge_adjacency[
        0, 2], "For boundary edge 0, edge a MUST equal edge c"
    assert edge_adjacency[0, 1] == edge_adjacency[
        0, 3], "For boundary edge 0, edge b MUST equal edge d"


